                                <h3>Description</h3>
                            </div>
                        </div>
                        <p>
                            This package serves to infer the network structure of the microbiome count data generated by
                            next-generation sequencing (NGS) technology. It also provides functions to generate
                            overdispersed and zero-inflated count data from a Dirichlet Multinomial model with flexible
                            parameter settings. The simulated count data also preserve a pre-specified network structure
                            under the Gaussian graphical model.
                        </p>
                    </article>

