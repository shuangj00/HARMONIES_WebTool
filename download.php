                    <article id="navarguments">
                        <div class="post-video">
                            <div class="post-heading">
                                <h3>Arguments</h3>
                            </div>
                        </div>
                        <div>
                            <table class="table table-hover">
                                <tbody>
                                <tr class="table-light">
                                    <td>count.matrix</td>
                                    <td>A count matrix from metagenomic shotgun sequencing or 16SrRNA sequencing
                                        technologies. Columns represent the taxa from the same taxonomic level, and rows
                                        represent the samples
                                    </td>
                                </tr>
                                <tr class="table-active">
                                    <td>phenotype</td>
                                    <td>A phenotype vector for all the samples. It should be a vector of 0s if only have
                                        one phenotype, or a vector of 0 and 1 if have 2 phenotypes
                                    </td>
                                </tr>
                                <tr class="table-light">
                                    <td>N.mcmc</td>
                                    <td>Number of MCMC iterations
                                    </td>
                                </tr>
                                <tr class="table-active">
                                    <td>b</td>
                                    <td>Shape parameter used in the hyper-parameter settings of the variance term
                                    </td>
                                </tr>
                                <tr class="table-light">
                                    <td>h</td>
                                    <td>Scale parameter used in the hyper-parameter settings of the variance term
                                    </td>
                                </tr>
                                <tr class="table-active">
                                    <td>sparsity.cutoff</td>
                                    <td>A threshold between 0-1. Taxa with proportions of zeros larger than the
                                        threshold would be dropped for the network inference
                                    </td>
                                </tr>
                                <tr class="table-light">
                                    <td>beta.stars</td>
                                    <td>Stability threshold for selection criteria
                                    </td>
                                </tr>
                                <tr class="table-active">
                                    <td>n.rep</td>
                                    <td>Number of random subsamples to take for network estimation
                                    </td>
                                </tr>
                                <tr class="table-light">
                                    <td>bayes.fdr</td>
                                    <td>Bayesian false discovery rate controlled for zero imputation</td>
                                </tr>
                                <tr class="table-light">
                                    <td>seed</td>
                                    <td>Random seed</td>
                                </tr>
                                </tbody>
                            </table>
                        </div>
